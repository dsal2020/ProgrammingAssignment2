## these functions create a matrix for which the inverse can
## be solved and stored

## make Cache matrix t
##takes any matrix and creates functions that define 
## and retrieve the matrix and its inverse 

makeCacheMatrix<- function(x = matrix()) {
  i <- NULL
  set <- function(y) {
    x <<- y
    i <<- NULL
  }
  get <- function() x
  setinverse <- function(inverse) i <<- inverse
  getinverse <- function() i
  list(set = set, get = get,
       setinverse = setinverse,
       getinverse = getinverse)
}

## checkes whether the inverse is cached, if not 
## solves for the inverse (works for square matrix only)

cacheSolve <- function(x, ...) {
  i <- x$getinverse()
  if(!is.null(i)) {
    message("getting cached data")
    return(i)
  }
  data <- x$get()
  i <- solve(data, ...)
  x$setinverse(i)
  i
}